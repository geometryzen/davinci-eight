/**
 * Cartesian coordinates of a bivector using the standard basis.
 */
export interface BivectorE3 {
    /**
     * The Cartesian yz-coordinate.
     */
    yz: number;
    /**
     * The Cartesian zx-coordinate.
     */
    zx: number;
    /**
     * The Cartesian xy-coordinate.
     */
    xy: number;
}
