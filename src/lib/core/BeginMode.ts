/**
 * The enumerated modes of drawing WebGL primitives.
 * 
 * https://www.khronos.org/registry/webgl/specs/1.0/
 */
export enum BeginMode {
    POINTS = 0x0000,
    LINES = 0x0001,
    LINE_LOOP = 0x0002,
    LINE_STRIP = 0x0003,
    TRIANGLES = 0x0004,
    TRIANGLE_STRIP = 0x0005,
    TRIANGLE_FAN = 0x0006
}
